# 6012 : [기초-입출력] 정수 2개 입력받아 그대로 출력하기1(py)
# 줄을 바꿔 정수(integer) 2개를 입력받아 줄을 바꿔 출력해보자.

# 예시
# a = input() 
# b = input()
# a=int(a)
# b=int(b)
# print(a)
# print(b)
# 과 같은 방법으로 가능하다.
 
m = input()
n = input()
m = int(m)
n = int(n)

print(m)
print(n)


# 풀이
# 첫번째 줄에 입력받은 값을 변수 m에 저장(할당,대입)
# 두번째 줄에 입력받은 값을 변수 n에 저장 
# 변수 m에 저장된 값을 정수로 변환하여 다시 저장
# 변수 n에 저장된 값을 정수로 변환하여 다시 저장
# 변수 m 출력
# 그 다음줄에 변수 n 출력